export const DEFAULT_BOOK_LANGUAGE: BookLanguage = 'da';

type EraPart = 'early' | 'mid' | 'late';

const eraPart = (year: number): EraPart => {
  const hundreds = Math.floor(year / 100) * 100;
  const within = year - hundreds;
  return within < 33 ? 'early' : within < 66 ? 'mid' : 'late';
};

const ordinal = (n: number): string => {
  const suffixes = ['th', 'st', 'nd', 'rd'];
  const value = n % 100;
  return n + (suffixes[(value - 20) % 10] || suffixes[value] || suffixes[0]);
};

const eraLabelEn = (year: number): string => {
  const century = Math.floor((year - 1) / 100) + 1;
  const within = (year - 1) % 100;
  const part = within < 33 ? 'early' : within < 66 ? 'mid' : 'late';
  return `${part} ${ordinal(century)} century`;
};

const eraLabelScand = (year: number, partMap: Record<EraPart, string>, suffix: string): string => {
  const hundreds = Math.floor(year / 100) * 100;
  return `${partMap[eraPart(year)]} ${hundreds}-${suffix}`;
};

