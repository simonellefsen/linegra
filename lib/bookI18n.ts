export interface BookStrings {
  languageName: string;
  // chrome
  familyHistory: (surname: string) => string;
  theFamily: (surname: string) => string;
  narrativeChronicle: (span: string) => string;
  coverLabel: string;
  contents: string;
  lives: (n: number) => string;
  generatedBy: string;
  spanPhrase: (earliest: number | null, latest: number | null) => string;
  eraLabel: (year: number) => string;
  // deterministic person biography fragments
  bio: {
    born: (name: string, year: string | null, place: string, parents: string) => string;
    married: (spouse: string) => string;
    partner: (partner: string) => string;
    raised: (n: number, names: string) => string;
    workedAs: (occupation: string) => string;
    fewRecords: (name: string) => string;
    died: (name: string, year: string, place: string, age: number | null) => string;
    deathUnknown: (name: string) => string;
    legacy: (name: string) => string;
  };
  // deterministic historical context
  hist: {
    spanYears: (years: number, era: string) => string;
    spanEra: (era: string) => string;
    witness: (place: string) => string;
    occupation: (occupation: string) => string;
  };
  // deterministic overview chapter
  overview: {
    family: (surname: string) => string;
    chronicles: (family: string, count: number, span: string, places: string) => string;
    acrossGens: (n: number) => string;
    acrossYears: string;
    witness: (genClause: string, family: string) => string;
    trades: (trades: string) => string;
    following: (treeName: string | null) => string;
  };
}

