// Localization for AI Family Books. Danish is the default (the app imports Danish/Swedish .ged
// trees); Swedish, Norwegian, and English are also supported.
//
// This module holds the book-chrome strings (cover, contents, titles) AND the deterministic
// narrative fragments, so that a book is fully localized even when it falls back to the
// deterministic composers (no API key). The AI composers additionally instruct the model to write
// in the chosen language. Keep the templates simple and natural; they take pre-formatted values.

